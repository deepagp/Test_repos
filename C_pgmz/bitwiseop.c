/*Example #1: Bitwise AND ,Bitwise OR operator,Bitwise XOR,Bitwise complement operator */

#include <stdio.h>
int main()
{
    int a = 12, b = 25;
/* Bitwise AND */

    printf("Output = %d", a&b);
/*Bitwise OR operator*/
    printf("Output = %d", a|b);
/*Bitwise XOR*/
    printf("Output = %d", a^b);
/*Bitwise complement operator */
    printf("Output = %d\n",~35);
    printf("Output = %d\n",~-12);
    return 0;
}


